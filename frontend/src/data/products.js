// Helper function to get product by ID
export const getProductById = (id) => {
  return productDetails.find(product => product.id === parseInt(id));
};

// Helper function to get related products (excluding current product)
export const getRelatedProducts = (currentProductId, limit = 4) => {
  return productDetails
    .filter(product => product.id !== parseInt(currentProductId))
    .slice(0, limit);
};
